const ensureMediaUnlocked = async (): Promise<boolean> => {
  const AudioContextClass = window.AudioContext || (window as any).webkitAudioContext
  if (!AudioContextClass) {
    console.warn('ÂΩìÂâçÊµèËßàÂô®‰∏çÊîØÊåÅ AudioContextÔºåË∑≥ËøáËß£ÈîÅÈÄªËæë')
    videoPlaybackUnlocked.value = true
    return true
  }

  try {
    // 1. Ëß£ÈîÅ AudioContext
    const audioContext = new AudioContextClass()
    if (audioContext.state === 'suspended') {
      await audioContext.resume()
    }

    const durationSeconds = 0.2
    const sampleRate = audioContext.sampleRate
    const frameCount = Math.max(1, Math.floor(sampleRate * durationSeconds))

    const buffer = audioContext.createBuffer(1, frameCount, sampleRate)
    const source = audioContext.createBufferSource()
    source.buffer = buffer
    source.connect(audioContext.destination)

    const playbackPromise = new Promise<void>((resolve) => {
      source.onended = () => resolve()
    })

    source.start()
    await playbackPromise

    source.disconnect()
    await audioContext.close()
    
    // Ê†áËÆ∞‰∏∫Â∑≤Ëß£ÈîÅÔºåÂêéÁª≠‰ºöÂú®ÂæÖÊú∫ËßÜÈ¢ëÊí≠ÊîæÊó∂Ëøõ‰∏ÄÊ≠•Ëß£ÈîÅ
    videoPlaybackUnlocked.value = true
    console.log('‚úÖ Èü≥È¢ëÊùÉÈôêÂ∑≤Ëß£ÈîÅ')
    return true
  } catch (error) {
    console.warn('Ëß£ÈîÅÂ™í‰ΩìÊí≠ÊîæÂ§±Ë¥•:', error)
    message.warning('ÊµèËßàÂô®ÈòªÊ≠¢‰∫ÜÂ™í‰ΩìÊí≠ÊîæÔºåËØ∑ÂÜçÊ¨°ÁÇπÂáª"ÂºÄÂßãÂØπËØù"ÊåâÈíÆ')
    return false
  }
}

